package com.kiarsy.todo.hexagonal.infrastructure.infrastructure.utils;

public class TimeUnitHelper {
    public final static long ONE_SEC_MILI = 1 * 1000;
    public final static long ONE_MIN_MILI = ONE_SEC_MILI * 60;
    public final static long ONE_HOUR_MILI = ONE_MIN_MILI * 60;
    public final static long ONE_DAY_MILI = ONE_HOUR_MILI * 24;
    public final static long ONE_WEEK_MILI = ONE_DAY_MILI * 7;
    public final static long ONE_MONTH_MILI = ONE_DAY_MILI * 30;
}
